package Switch;

public class Switch1 {

	public static void main(String[] args) {
	 int i= 2;
	 switch(i) {
	 case 1:
		 System.out.println("value is one");
		 break;
	 case 2:
		 System.out.println("value is two");
		 break;
	 case 3:	
		 System.out.println("value is three");
		break;
	default:
		System.out.println("value is not found");	 
	 }
    
	}

}
