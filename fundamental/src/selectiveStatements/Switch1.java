package selectiveStatements;

public class Switch1 {

	public static void main(String[] args) {
		String s1 = "ambe";
		switch (s1) {
		case "anjali":
			System.out.println("value is anjali");
			break;
		case "anu":
			System.out.println("value is anu");
			break;
		case "ambe":
			System.out.println("value is ambe");
			break;
		default:
			System.out.println("none of the above");

		}
	}

}
